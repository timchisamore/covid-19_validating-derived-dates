#' Reading CCM case symptoms data
#'
#' This function uses the read_csv() function from the readr package to read
#' the raw CCM case symptoms data in.
#'
#' @param ccm_case_symptoms_path A character representing the path to the raw
#' CCM case symptoms data.
#'
#' @return A tbl_df of raw CCM case symptoms data.
#' @export
#'
#' @examples
#' `reading_ccm_case_symptoms_data(here::here("data", "raw", "ccm_case_symptoms_data.csv"))`
reading_ccm_case_symptoms_data <- function(ccm_case_symptoms_path) {
  read_csv(
    file = ccm_case_symptoms_path,
    col_names = TRUE,
    col_types = cols(
      `Investigation Number` = col_character(),
      `Abdominal Pain` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Abdominal Pain Onset Date` = col_character(),
