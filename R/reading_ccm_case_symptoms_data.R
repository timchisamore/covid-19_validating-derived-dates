      `Hypoxia (unexplained) Onset Date` = col_character(),
      `Hypoxia (unexplained) Recovery Date` = col_character(),
      `Difficulty feeding in infants` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Difficulty feeding in infants Onset Date` = col_character(),
      `Difficulty feeding infants Recovery Date` = col_character(),
      `Other<U+FFFD>Symptoms` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Other Symptom Onset Date` = col_character(),
      `Recovery Date Other Symp` = col_character(),
      `Fatigue` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Fatigue Onset Date` = col_character(),
      `Fatigue Recovery Date` = col_character(),
      `Fever (Not Recorded)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Fever (Not Recorded) Onset Date` = col_character(),
      `Hoarse Voice` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Hoarse Voice Onset Date` = col_character(),
      `Hoarse Voice Recovery Date` = col_character(),
      `Lethargy` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Lethargy Onset Date` = col_character(),
      `Lethargy Recovery Date` = col_character(),
      `Lymph Node Swelling/Pain(Lymphadenopath)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Node Swelling Onset Date` = col_character(),
      `Node Swelling Recovery Date` = col_character(),
      `Malaise (General Unwell Feeling)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Malaise Onset Date` = col_character(),
      `Malaise Recovery Date` = col_character(),
      `Myalgia (Muscle Pain)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Myalgia Onset Date` = col_character(),
      `Myalgia Recovery Date` = col_character(),
      `New olfactory or taste disorder` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `New OOTD Onset Date` = col_character(),
      `New OOTD Recovery Date` = col_character(),
      `Palpitations` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Palpitations Onset Date` = col_character(),
      `Palpitations Recovery Date` = col_character(),
      `Sneezing` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Sneezing Onset Date` = col_character(),
      `Sneezing Recovery Date` = col_character(),
      `Tachypnea (Rapid Breathing)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Tachypnea Onset Date` = col_character(),
      `Tachypnea Recovery Date` = col_character(),
      `Delirium` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Delirium Start Date` = col_character(),
      `Delirium Recovery Date` = col_character(),
      `Asymptomatic` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE)
    )
  )
}
