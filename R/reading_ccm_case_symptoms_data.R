      `Acute functional decline Onset Date` = col_character(),
      `Acute FD Recovery Date` = col_character(),
      `Acutely Altered Mental Status` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Acutely AM Onset Date` = col_character(),
      `Acutely AM Recovery Date` = col_character(),
      `Anorexia (Loss of Appetite)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Anorexia Onset Date` = col_character(),
      `Anorexia Recovery Date` = col_character(),
      `Joint Pain (Arthralgia)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Arthralgia Onset Date` = col_character(),
      `Arthralgia Recovery Date` = col_character(),
      `Chest Pain` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Chest Pain Onset Date` = col_character(),
      `Chest Pain Recovery Date` = col_character(),
      `Chills` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Chills Onset Date` = col_character(),
      `Chills Recovery Date` = col_character(),
      `Pink Eye (Conjunctivitis)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Pink Eye (Conjunctivitis) Onset Date` = col_character(),
      `Pink Eye (Conjunctivitis) Recovery Date` = col_character(),
      `Cough (New or worsening)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Cough (New or worsening) Onset Date` = col_character(),
      `Cough (New or worsening) Recovery Date` = col_character(),
      `Cough (Not new or worsening)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Cough Onset Date` = col_character(),
      `Cough Recovery Date` = col_character(),
      `Croup (Barking cough in children)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Croup Start Date` = col_character(),
      `Croup Recovery Date` = col_character(),
      `Decrease in blood pressure (Hypotension)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Hypotension Start Date` = col_character(),
      `Hypotension Recovery Date` = col_character(),
      `Decreased or Lack or Appetite` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Decreased or Lack or Appetite Onset Date` = col_character(),
      `Decreased or Lack Appetite Recovery Date` = col_character(),
      `Decrease or Loss of Smell or Taste` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Decrease of Smell or Taste Onset Date` = col_character(),
      `Decrease of Smell or Taste Recovery Date` = col_character(),
      `Diarrhea` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Diarrhea Onset Date` = col_character(),
      `Diarrhea Recovery Date` = col_character(),
      `Difficulty swallowing (Dysphagia)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Difficult Swallow Onset Date` = col_character(),
      `Difficult Swallow Recovery Date` = col_character(),
