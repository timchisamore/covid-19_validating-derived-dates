      `Dizziness Onset Date` = col_character(),
      `Dizziness Recovery Date` = col_character(),
      `Ear ache` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Ear Ache Onset Date` = col_character(),
      `Ear Ache Recovery Date` = col_character(),
      `Falls` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Falls Onset Date` = col_character(),
      `Recovery Date Falls` = col_character(),
      `Fatigue lethargy or malaise` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Fatigue, lethargy or malaise Onset Date` = col_character(),
      `Fatigue lethargy or malaise Recovry Date` = col_character(),
      `Fever` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Fever Onset Date` = col_character(),
      `Fever Recovery Date` = col_character(),
      `Headache` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Headache Onset Date` = col_character(),
      `Headache Recovery Date` = col_character(),
      `Muscle Aches and Pain` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Muscle Aches and Pain Onset Date` = col_character(),
      `Muscle Aches and Pain Recovery Date` = col_character(),
      `Nasal Congestion` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Nasal Congestion Onset Date` = col_character(),
      `Nasal Congestion Recovery Date` = col_character(),
      `Nausea` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Nausea Onset Date` = col_character(),
      `Nausea Recovery Date` = col_character(),
      `Runny nose` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Runny Nose OnSet Date` = col_character(),
      `Runny Nose Recovery Date` = col_character(),
      `Shortness of Breath` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Shortness of Breath Onset Date` = col_character(),
      `Shortness of Breath Recovery Date` = col_character(),
      `Sore Throat` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Sore Throat Onset Date` = col_character(),
      `Recovery Date SoreThroat` = col_character(),
      `Vomiting` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Vomiting Onset Date` = col_character(),
      `Vomiting Recovery Date` = col_character(),
      `New or Worsening Chronic Conditions` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `New or Worsening Chronic Onset Date` = col_character(),
      `New or Worsening Chronic Recovery Date` = col_character(),
      `Tachycardia (Fast Heart Rate)` = col_factor(levels = c("Yes", "No", "Unknown", "Not Reported"), include_na = TRUE),
      `Tachycardia Onset Date` = col_character(),
